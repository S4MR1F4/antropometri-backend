    /**
     * Interpolate intermediate months or steps to ensure complete data.
     */
    private static function fillIntermediate(array $data, int $maxKey): array
    {
        $keys = array_keys($data);
        sort($keys);

        $result = [];
        for ($i = 0; $i < count($keys) - 1; $i++) {
            $start = $keys[$i];
            $end = $keys[$i + 1];
            $startVal = $data[$start];
            $endVal = $data[$end];
            $steps = $end - $start;

            for ($j = 0; $j < $steps; $j++) {
                $currentKey = $start + $j;
                $row = [];
                foreach ($startVal as $idx => $val) {
                    $row[] = round($val + ($endVal[$idx] - $val) * ($j / $steps), 3);
                }
                $result[$currentKey] = $row;
            }
        }
        $result[$maxKey] = $data[$maxKey];
        return $result;
    }

