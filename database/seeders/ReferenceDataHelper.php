    /**
     * Interpolate intermediate steps based on a specific scale (e.g. 0.5 for height).
     */
    private static function fillIntermediateScale(array $data, float $maxKey, float $scale): array
    {
        $keys = array_keys($data);
        sort($keys);

        $result = [];
        for ($i = 0; $i < count($keys) - 1; $i++) {
            $start = $keys[$i];
            $end = $keys[$i + 1];
            $startVal = $data[$start];
            $endVal = $data[$end];
            $distance = $end - $start;
            $steps = (int) ($distance / $scale);

            for ($j = 0; $j < $steps; $j++) {
                $currentKey = $start + ($j * $scale);
                $row = [];
                foreach ($startVal as $idx => $val) {
                    $row[] = round($val + ($endVal[$idx] - $val) * ($j / $steps), 3);
                }
                // Use string key for float to avoid array indexing issues
                $result[(string) $currentKey] = $row;
            }
        }
        $result[(string) $maxKey] = $data[$maxKey];
        return $result;
    }
